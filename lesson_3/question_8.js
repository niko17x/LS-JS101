// How can we add the family pet, "Dino", to the following array?

let flintstones = ["Fred", "Barney", "Wilma", "Betty", "Bambam", "Pebbles"];

// SOLUTIONS:
flintstones.push("Dino"); // Pushes 'Dino' to array and mutates the original array data.

flintstones[flintstones.length] = "Dino"; // Adds 'Dino' to end of original array and mutates it.
